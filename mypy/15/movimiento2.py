#!/usr/bin/env python
import os
import time

espacios = ""

for i in range(20):
    time.sleep(.2)
    os.system("clear")

    print()
    print(f"{espacios}   o   ")
    print(f"{espacios}  /|\   ")
    print(f"{espacios}  / |   ")

    espacios += " "

    time.sleep(.2)
    os.system("clear")

    print()
    print(f"{espacios}   o   ")
    print(f"{espacios}  (|\   ")
    print(f"{espacios}  | |   ")

    espacios += " "

    time.sleep(.2)
    os.system("clear")

    print()
    print(f"{espacios}   o   ")
    print(f"{espacios}  /|\   ")
    print(f"{espacios}  | \   ")

    espacios += " "

    time.sleep(.2)
    os.system("clear")

    print()
    print(f"{espacios}   o   ")
    print(f"{espacios}  /|)   ")
    print(f"{espacios}  | |   ")

    espacios += " "